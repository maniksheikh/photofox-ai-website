// Reactive state variables
const selectedButton = ref(0);
const intervalId = ref(null);
const totalArtCount = ref(33594);
const totalUserCount = ref(23908);

// Lifecycle hook equivalent to 'created'
onMounted(() => {
    startStyleLoop();
    getArtAndUserCount();
});

// Computed property to calculate the total number of buttons
const totalButtonCount = computed(() => {
    return generates.reduce(
        (count, generate) => count + generate.output.length,
        0
    );
});

const selectButtonByIndex = (buttonIndex) => {
    selectedButton.value = buttonIndex;
};

const getArtAndUserCount = async () => {
    try {
        const response = await fetch(
            "https://scketch-to-art-backend-fcjuh3h5uq-uc.a.run.app/api/v1/art/count/all"
        );
        if (!response.ok) {
            throw new Error("Network response was not ok");
        }
        const data = await response.json();
        totalArtCount.value = data.totalArt;
        totalUserCount.value = data.totalUser;
    } catch (error) {
        console.log(error);
    }
};

const startStyleLoop = () => {
    intervalId.value = setInterval(() => {
        const nextButtonIndex = (selectedButton.value + 1) % totalButtonCount.value;
        selectButtonByIndex(nextButtonIndex);
    }, 3000);
};

// Expose the selectButton function if it needs to be used directly in template
const selectButton = (generate, buttonIndex) => {
    selectButtonByIndex(buttonIndex);
};

